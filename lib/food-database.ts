/**
 * Sucht Lebensmittel nach Name (case-insensitive, inklusive Teilstring).
 * Gibt maximal 50 Treffer zurück.
 */
export function searchFoods(query: string): FoodItem[] {
  if (query.trim() === '') return FOOD_DATABASE.slice(0, 50)
  const q = query.toLowerCase().trim()
  return FOOD_DATABASE.filter((item) =>
    item.name.toLowerCase().includes(q) ||
    FOOD_CATEGORY_LABELS[item.category].toLowerCase().includes(q)
  ).slice(0, 50)
}

/**
 * Findet ein Lebensmittel anhand seiner ID.
 */
export function getFoodById(id: string): FoodItem | undefined {
  return FOOD_DATABASE.find((item) => item.id === id)
}

/**
 * Berechnet die Nährwerte für eine gegebene Portionsgröße.
 */
export function calculateMacros(food: FoodItem, portionGrams: number): FoodMacros {
  const factor = portionGrams / 100
  return {
    calories: Math.round(food.per100g.calories * factor),
    proteinG: Math.round(food.per100g.proteinG * factor * 10) / 10,
    carbsG: Math.round(food.per100g.carbsG * factor * 10) / 10,
    fatG: Math.round(food.per100g.fatG * factor * 10) / 10,
    fiberG: Math.round(food.per100g.fiberG * factor * 10) / 10,
  }
}
